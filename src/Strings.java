// primitive and reference types
// reference type starts with Upper case
// string is a class
// classes are written with Upper case

public class Strings {
  public static void main(String[] args) {
    int x = 5;
    String myString = "Hello"; // myString is referenced to "Hello" which is an object
    String space = " ";
    String myName = "Bob";
    System.out.println(myString + space + myName);
    System.out.println("Hello" + " " + "John");
    System.out.println("My number is " + x);
  }
}
